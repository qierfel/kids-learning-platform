export default function Geography({ user }) {
  const [activeChapter, setActiveChapter] = useState('earth')
  const [expandedPoints, setExpandedPoints] = useState({})

  const chapter = CHAPTERS.find(c => c.id === activeChapter)

  function togglePoint(id) {
    setExpandedPoints(p => ({ ...p, [id]: !p[id] }))
  }

  return (
    <div className="junior-subject junior-subject--geography">
      <section className="junior-hero">
        <div>
          <div className="junior-kicker">Geography Route</div>
          <h1 className="junior-title">地理 <span className="junior-edition">山西中考 · 人教版</span></h1>
          <p className="junior-lead">
            把地球与地图、世界地理、中国地理和区域专题放到同一张路径图里，查资料和复习时都会更顺手。
          </p>
        </div>
        <div className="junior-meta-grid">
          <div className="junior-meta-card">
            <div className="junior-meta-label">推荐方式</div>
            <div className="junior-meta-value">先分世界 / 中国，再补区域专题</div>
          </div>
          <div className="junior-meta-card junior-meta-card--accent">
            <div className="junior-meta-label">学习重点</div>
            <div className="junior-meta-value">地图、气候、区域差异一起看</div>
          </div>
        </div>
      </section>

      <div className="chapter-tabs">
        {CHAPTERS.map(c => (
          <button
            key={c.id}
            className={`chapter-tab ${activeChapter === c.id ? 'active' : ''}`}
            onClick={() => setActiveChapter(c.id)}
          >
            {c.icon} {c.title}
          </button>
        ))}
      </div>

      <div className="knowledge-list">
        {chapter.points.map((p, i) => {
          const key = `${activeChapter}-${i}`
          const open = expandedPoints[key]
          return (
            <div key={key} className="knowledge-card" onClick={() => togglePoint(key)}>
              <div className="knowledge-header">
                <span className="knowledge-title">{p.title}</span>
                <span className="expand-icon">{open ? '▲' : '▼'}</span>
              </div>
              {open && (
                <div className="knowledge-body">
                  <div className="knowledge-content" dangerouslySetInnerHTML={{ __html: renderMd(p.content) }} />
                  {p.formula && (
                    <div className="formula-box">
                      <span className="formula-label">公式：</span>{p.formula}
                    </div>
                  )}
                  {p.exam && (
                    <div className="exam-tip">
                      <span className="exam-label">中考考点</span>
                      {p.exam}
                    </div>
                  )}
                </div>
              )}
            </div>
          )
        })}
      </div>
    </div>
  )
}

function renderMd(text) {
  return text.replace(/\*\*(.+?)\*\*/g, '<strong>$1</strong>')
}
